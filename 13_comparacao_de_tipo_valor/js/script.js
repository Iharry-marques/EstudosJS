// Comparação de tipo e valor
/*
    * Dois outros operadores de comparação existem no JavaScript: === e !==;
    * A função é comparar valor e tipo de dado;
*/

var numero = '5';

if (numero === '5'){
    console.log("O numero é 5")
}

if (numero !== 5){
    console.log("Não é o numero 5 do tipo number")
}